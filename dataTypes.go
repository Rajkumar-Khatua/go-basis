package main

import "fmt"

func main() {
    // Integer data types
    var age int = 30
    var height uint = 175
    var weight float64 = 70.5
    var isMale bool = true
    var name string = "John Doe"
    
    // Print variables
    fmt.Println("Age:", age)
    fmt.Println("Height:", height)
    fmt.Println("Weight:", weight)
    fmt.Println("Is Male:", isMale)
    fmt.Println("Name:", name)
}
